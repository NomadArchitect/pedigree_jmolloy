import SCons


def HeaderFromFileGenerator(target, source, env):
    contents =  '/* Made from " + source[0].path + " */\n'
    contents += '/* Autogenerated by the build system, do not edit. */\n'

    contents += 'static uint8_t file[] = {\n'
    with open(source[0].path, 'rb') as src:
        source = src.read()
        for byte in source:
            contents += '0x%02x,\n' % ord(byte)
    contents += '};\n'
    with open(target[0].path, 'wb') as dst:
        dst.write(contents)


def generate(env):
    action = SCons.Action.Action(HeaderFromFileGenerator, env.get('TARCOMSTR'))
    builder = env.Builder(action=action, target_factory=env.File,
        source_factory=env.Value, single_source=1)

    env.Append(BUILDERS={'FileAsHeader': builder})
